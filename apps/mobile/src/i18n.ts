const dictionary: Record<AppLanguage, Record<keyof typeof fr, string>> = { fr, en };

export type TranslationKey = keyof typeof fr;

export function normalizeLanguagePreference(value: unknown): LanguagePreference {
  return value === 'fr' || value === 'en' || value === 'system' ? value : 'system';
}

export function languageFromLocale(locale: string | null | undefined): AppLanguage {
  const normalized = String(locale || '').trim().toLowerCase();
  if (normalized.startsWith('en')) {
    return 'en';
  }
  if (normalized.startsWith('fr')) {
    return 'fr';
  }
  return FALLBACK_LANGUAGE;
}

export function getSystemLocaleTags(): string[] {
  const intlLocale = Intl.DateTimeFormat().resolvedOptions().locale;
  return intlLocale ? [intlLocale] : [];
}

export function resolveAppLanguage(preference: LanguagePreference, locales: string[] = getSystemLocaleTags()): AppLanguage {
  if (preference === 'fr' || preference === 'en') {
    return preference;
  }
  return languageFromLocale(locales[0]);
}

export function t(language: AppLanguage, key: TranslationKey): string {
  return dictionary[language]?.[key] ?? dictionary[FALLBACK_LANGUAGE][key];
}
